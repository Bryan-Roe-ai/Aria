#!/usr/bin/env python3
"""Repair corrupted JSON status files under data_out/.

Some long-running orchestrators write status.json incrementally; merge conflicts
can leave git conflict markers in these runtime artifacts and break downstream
health agents. This utility resolves conflict markers (keeping the first/HEAD
section), validates JSON, and optionally refreshes stale timestamps.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from datetime import datetime, timezone
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_DATA_DIR = REPO_ROOT / "data_out"
TIMESTAMP_FIELDS = ("last_updated", "timestamp", "updated_at", "last_run", "completed_at", "generated_at")
TRAILING_COMMA_PATTERN = re.compile(r",(\s*[}\]])")


def cleanup_json_text(text: str) -> str:
    """Remove trailing commas before closing JSON braces/brackets."""
    cleaned = text
    while True:
        next_text = TRAILING_COMMA_PATTERN.sub(r"\1", cleaned)
        if next_text == cleaned:
            return cleaned
        cleaned = next_text

# Agent output directories are regenerated by the agent runner.
AGENT_OUTPUT_PARTS = frozenset({"agents"})


def utc_now_iso() -> str:
    """Return the current UTC time as an ISO-8601 string."""
    return datetime.now(timezone.utc).isoformat()


def resolve_merge_conflicts(text: str) -> tuple[str, int]:
    """Resolve git conflict markers, keeping the HEAD (first) section each time."""
    lines = text.splitlines(keepends=True)
    rounds = 0

    while any(line.startswith("<<<<<<<") for line in lines):
        output: list[str] = []
        index = 0
        changed = False

        while index < len(lines):
            line = lines[index]
            if not line.startswith("<<<<<<<"):
                output.append(line)
                index += 1
                continue

            changed = True
            index += 1
            head_lines: list[str] = []
            while index < len(lines) and lines[index].strip() != "=======":
                head_lines.append(lines[index])
                index += 1
            if index >= len(lines):
                output.extend(head_lines)
                break
            index += 1  # skip =======

            depth = 1
            while index < len(lines) and depth > 0:
                if lines[index].startswith("<<<<<<<"):
                    depth += 1
                elif lines[index].startswith(">>>>>>>"):
                    depth -= 1
                index += 1

            output.extend(head_lines)

        if not changed:
            break
        lines = output
        rounds += 1

    return "".join(lines), rounds


def _is_agent_output(path: Path, data_dir: Path) -> bool:
    try:
        parts = path.relative_to(data_dir).parts
    except ValueError:
        parts = path.parts
    return "agents" in parts[:-1] or path.parent.name == "agents"


def _parse_timestamp(value: object) -> datetime | None:
    if not isinstance(value, str) or not value.strip():
        return None
    normalized = value.strip()
    if normalized.endswith("Z"):
        normalized = f"{normalized[:-1]}+00:00"
    try:
        parsed = datetime.fromisoformat(normalized)
    except ValueError:
        return None
    if parsed.tzinfo is None:
        return parsed.replace(tzinfo=timezone.utc)
    return parsed.astimezone(timezone.utc)


def _find_timestamp(payload: object) -> datetime | None:
    if not isinstance(payload, dict):
        return None
    for field in TIMESTAMP_FIELDS:
        parsed = _parse_timestamp(payload.get(field))
        if parsed is not None:
            return parsed
    return None


def _refresh_timestamps(payload: dict) -> bool:
    """Refresh recognized timestamp fields when stale; return whether changed."""
    now_text = utc_now_iso()
    timestamp = _find_timestamp(payload)
    if timestamp is None:
        payload["updated_at"] = now_text
        return True

    age_hours = (datetime.now(timezone.utc) - timestamp).total_seconds() / 3600
    if age_hours <= 24:
        return False

    for field in TIMESTAMP_FIELDS:
        if field in payload:
            payload[field] = now_text
    if "updated_at" not in payload:
        payload["updated_at"] = now_text
    return True


def _minimal_reset_payload() -> dict:
    """Return a minimal valid status payload after unrecoverable corruption."""
    now = utc_now_iso()
    return {
        "updated_at": now,
        "last_updated": now,
        "status": "reset",
        "note": "Reset by repair_data_out_status after unresolvable merge conflicts",
    }


def repair_status_file(path: Path, *, refresh_stale: bool = False) -> dict:
    """Repair one status file and return a result summary."""
    original = path.read_text(encoding="utf-8")
    resolved, conflict_rounds = resolve_merge_conflicts(original)
    resolved = cleanup_json_text(resolved)
    reset = False

    if "<<<<<<<" in resolved:
        resolved, extra_rounds = resolve_merge_conflicts(resolved)
        conflict_rounds += extra_rounds

    try:
        payload = json.loads(resolved)
    except json.JSONDecodeError:
        if "<<<<<<<" in resolved or "<<<<<<<" in original:
            payload = _minimal_reset_payload()
            reset = True
            conflict_rounds = conflict_rounds or 1
        else:
            return {
                "file": str(path),
                "status": "failed",
                "detail": "Invalid JSON and no merge conflict markers found",
                "conflicts_resolved": conflict_rounds,
            }

    refreshed = False
    if isinstance(payload, dict):
        if _find_timestamp(payload) is None:
            payload["updated_at"] = utc_now_iso()
            refreshed = True
        elif refresh_stale:
            refreshed = _refresh_timestamps(payload)

    changed = reset or refreshed or resolved != original
    if changed:
        path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")

    status = "repaired" if changed else "ok"
    detail_parts: list[str] = []
    if reset:
        detail_parts.append("reset to minimal valid status payload")
    if conflict_rounds:
        detail_parts.append(f"resolved {conflict_rounds} conflict block(s)")
    if refreshed:
        detail_parts.append("refreshed stale timestamp(s)")
    detail = "; ".join(detail_parts) if detail_parts else "no changes needed"

    return {
        "file": str(path),
        "status": status,
        "detail": detail,
        "conflicts_resolved": conflict_rounds,
        "refreshed": refreshed,
        "reset": reset,
    }


def iter_status_files(data_dir: Path) -> list[Path]:
    """Return sorted status.json paths excluding agent output directories."""
    if not data_dir.exists():
        return []
    paths = []
    for path in sorted(data_dir.rglob("status.json")):
        if _is_agent_output(path, data_dir):
            continue
        paths.append(path)
    return paths


def repair_all(data_dir: Path, *, refresh_stale: bool = False) -> list[dict]:
    """Repair all orchestrator status files under *data_dir*."""
    results: list[dict] = []
    for path in iter_status_files(data_dir):
        try:
            result = repair_status_file(path, refresh_stale=refresh_stale)
            try:
                result["file"] = str(path.relative_to(REPO_ROOT))
            except ValueError:
                result["file"] = str(path)
        except OSError as exc:
            result = {"file": str(path), "status": "failed", "detail": str(exc)}
        results.append(result)
    return results


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Repair data_out status.json merge conflicts.")
    parser.add_argument(
        "--data-dir",
        type=Path,
        default=DEFAULT_DATA_DIR,
        help="Directory to scan (default: data_out/)",
    )
    parser.add_argument(
        "--refresh-stale",
        action="store_true",
        help="Refresh timestamp fields older than 24 hours.",
    )
    parser.add_argument("--json", action="store_true", help="Print results as JSON.")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    results = repair_all(args.data_dir, refresh_stale=args.refresh_stale)

    repaired = sum(1 for item in results if item.get("status") == "repaired")
    failed = sum(1 for item in results if item.get("status") == "failed")

    if args.json:
        print(json.dumps({"results": results, "repaired": repaired, "failed": failed}, indent=2))
    else:
        for item in results:
            if item.get("status") != "ok":
                print(f"[{item['status']}] {item['file']}: {item.get('detail', '')}")
        print(f"[repair_data_out_status] scanned={len(results)} repaired={repaired} failed={failed}")

    return 1 if failed else 0


if __name__ == "__main__":
    raise SystemExit(main())
